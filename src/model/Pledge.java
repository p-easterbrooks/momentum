package model;
    
/**
 * This class defines attributes for a pledge
 * @author pauleasterbrooks
 */
public class Pledge {
    private String name, email, city, state, country;
    
    public Pledge(String name, String email, String city, String state, String country)
    {
        this.name = name;
        this.email = email;
        this.city = city;
        this.state = state;
        this.country = country;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }


    
}
